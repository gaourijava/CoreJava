package ExampleOfSir2;

public class Student  //Non Primitive Data Type
{
	int rollno;
	String name;
	public void setRollno(int rollno)
	{
		this.rollno=rollno;
	}
	public int getRollno()
	{
		return rollno;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
}
