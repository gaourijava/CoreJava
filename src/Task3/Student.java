package Task3;

public class Student   //NonPrimitive Data type
{
	private int rollno;
	private String name;
	private String add;
	
	
	public void setRollno(int rollno)
	{
		this.rollno=rollno;
	}
	public int getRollno()
	{
		return rollno;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
	public void setAdd(String add)
	{
		this.add=add;
	}
	public String getAdd()
	{
		return add;
	}
	
}
